package com.Pisquare.Beans;

public class Branch_Master {
	
	
	private int Branch_Code;   
	private String Branch_Name;
	private String Branch_Address;
	private String Branch_City;
	private String Branch_Pincode;
	private String Branch_Phone;
	private long Branch_CusIdFrom;
	private long Branch_AccNoFrom;
	
	public Branch_Master(){}
	
	
	public int getBranch_Code()
	{
		return Branch_Code;
	}
	
	public void setBranch_Code(int Branch_Code)
	{
		this.Branch_Code=Branch_Code;
	}

	
	
	
	
	public String getBranch_Name()
	{
		return Branch_Name;
	}
	
	public void setBranch_Name(String Branch_Name)
	{
		this.Branch_Name=Branch_Name;
	}

	
	
	
	
	
	public String getBranch_Address()
	{
		return Branch_Address;
	}
	
	public void setBranch_Address(String Branch_Address)
	{
		this.Branch_Address=Branch_Address;
	}
	
	
	
	
	public String getBranch_City()
	{
		return Branch_City;
	}
	
	public void setBranch_City(String Branch_City)
	{
		this.Branch_City=Branch_City;
	}
	
	
	
	
	
	public String getBranch_Pincode()
	{
		return Branch_Pincode;
	}
	
	public void setBranch_Pincode(String Branch_Pincode)
	{
		this.Branch_Pincode=Branch_Pincode;
	}
	
	
	
	
	public String getBranch_Phone()
	{
		return Branch_Phone;
	}
	
	public void setBranch_Phone(String Branch_Phone)
	{
		this.Branch_Phone=Branch_Phone;
	}
	
	
	
	
	
	
	public long getBranch_CusIdFrom()
	{
		return Branch_CusIdFrom;
	}
	
	public void setBranch_CusIdFrom(long Branch_CusIdFrom)
	{
		this.Branch_CusIdFrom=Branch_CusIdFrom;
	}
	
	
	
	
	public long getBranch_AccNoFrom()
	{
		return Branch_AccNoFrom;
	}
		
	public void setBranch_AccNoFrom(long Branch_AccNoFrom)
	{
		this.Branch_AccNoFrom=Branch_AccNoFrom;
	}
	
}
