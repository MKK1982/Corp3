package com.Pisquare.Beans;

public class GL_Master {

	
	private int GL_Code;
	private String GL_Name;
	private String GL_Schedule;
	private String GL_Category;
	
	
	
	
	
	public GL_Master()
	{
		
	}
	
	
	
	
	public int getGL_Code()
	{
		return GL_Code;
		
	}
	
	public void setGL_Code(int GL_Code)
	{
		this.GL_Code=GL_Code;
	}
	
	
	public String getGL_Name()
	{
		return GL_Name;
		
	}
	
	public void setGL_Name(String GL_Name)
	{
		this.GL_Name=GL_Name;
	}
	
	
	
	
	public String getGL_Schedule()
	{
		return GL_Schedule;
		
	}
	
	public void setGL_Schedule(String GL_Schedule)
	{
		this.GL_Schedule=GL_Schedule;
	}
	
	
	
	public String getGL_Category()
	{
		return GL_Category;
		
	}
	
	public void setGL_Category(String GL_Category)
	{
		this.GL_Category=GL_Category;
	}
	
	
}
